CREATE TABLE products (
    id INT AUTO_INCREMENT PRIMARY KEY,  -- Campo autoincremental e identificador principal
    name VARCHAR(255) NOT NULL,         -- Nombre del producto (string)
    price INT NOT NULL,                  -- Precio del producto (entero/integer)
    description TEXT                    -- Descripción detallada del producto (string de longitud variable)
);


